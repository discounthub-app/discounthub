# üß™ –ü—Ä–∏–º–µ—Ä –ø—Ä—è–º–æ–≥–æ SQL-–∑–∞–ø—Ä–æ—Å–∞ (–¥–ª—è –æ—Ç–ª–∞–¥–∫–∏)
@app.get("/users/raw")
def get_users():
    db: Session = SessionLocal()
    users = db.execute(text("SELECT id, username, email FROM users")).mappings().all()
    db.close()
    return users
